  async _generateTauriConf(projectName) {
    const tauriConf = {
      ...this.templates.tauriConf,
      package: {
        ...this.templates.tauriConf.package,
        productName: projectName
      }
    };

    // Replace placeholders
    const confString = JSON.stringify(tauriConf, null, 2)
      .replace(/{{APP_NAME}}/g, projectName.toLowerCase().replace(/\s+/g, '-'))
      .replace(/{{APP_TITLE}}/g, projectName);

    return {
      fileName: 'src-tauri/tauri.conf.json',
      content: confString
    };
  }

